<h1> Użytkowanie aplikacji</h1>
<p>
    Po wybraniu zestawu słownictwa wyświetla się pierwsze słowo. Mozna wtedy spróbować je sobie przypomnieć bądź zamigać.
    Można wtedy nacisnąć "sprawdź", by zobaczyć jak wygląda poprawnie zamigany znak. Jeżeli dobrze pamiętało się znak,
    należy wybrać "Znam". Wtedy aplikacja nie zapyta już ponownie o to słowo. Jeżeli nie pamiętało się znaku, bądź
    dla utrwalenia chce się go jeszcze później ponownie zobaczyć, należy wybrać "Nie znam". Wtedy aplikacja zapyta nas o
    inne słowo, ale za jakiś czas wróci do obecnego.
</p>

